package sec03.exam02;

public class SwitchTest {

	public static void main(String[] args) {
		double val = 1;
		switch(val) {
		case 1:
			System.out.println("Code 1");
		case 2:
			System.out.println("Code 2");
		case 3:
			System.out.println("Code 3");
			break;
			default:
				System.out.println("No code");
		}

	}

}